import { execSync } from "node:child_process";
import { mkdirSync, readdirSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");
const schemasDir = path.join(rootDir, "packages/api-contracts/schemas");
const webDir = path.join(rootDir, "apps/web");
const apiDir = path.join(rootDir, "apps/api");
const webGeneratedDir = path.join(webDir, "lib/api/generated");
const apiGeneratedDir = path.join(apiDir, "schemas/generated");

/** Stable banner without eslint-disable — keep ESLint on generated types. */
const TS_BANNER = `/**
 * This file was automatically generated by json-schema-to-typescript.
 * DO NOT MODIFY IT BY HAND. Instead, modify the source JSONSchema file,
 * and run json-schema-to-typescript to regenerate this file.
 */`;

/**
 * recommendations.request → recommendationRequestSchema
 * recommendations.response → recommendationResponseSchema
 */
function toZodExportName(schemaBaseName) {
  const [resource, kind] = schemaBaseName.split(".");
  const singular = resource.endsWith("s") ? resource.slice(0, -1) : resource;
  const camel = singular + kind.charAt(0).toUpperCase() + kind.slice(1);
  return `${camel}Schema`;
}

/** recommendations.request → recommendations_request.py */
function toPythonModuleName(schemaBaseName) {
  return schemaBaseName.replaceAll(".", "_");
}

/** recommendations.request → RecommendationsRequest */
function toPythonClassName(schemaBaseName) {
  return schemaBaseName
    .split(".")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");
}

function run(command, cwd = rootDir) {
  execSync(command, { cwd, stdio: "inherit" });
}

/** Single-quote for POSIX shells so multiline banners keep real newlines. */
function shellQuote(value) {
  return `'${String(value).replace(/'/g, `'\\''`)}'`;
}

const schemaFiles = readdirSync(schemasDir)
  .filter((file) => file.endsWith(".json"))
  .sort();

if (schemaFiles.length === 0) {
  throw new Error(`No JSON schemas found in ${schemasDir}`);
}

mkdirSync(webGeneratedDir, { recursive: true });
mkdirSync(apiGeneratedDir, { recursive: true });

for (const schemaFile of schemaFiles) {
  const schemaPath = path.join(schemasDir, schemaFile);
  const baseName = schemaFile.replace(/\.json$/, "");
  const zodExportName = toZodExportName(baseName);

  const tsOutput = path.join(webGeneratedDir, `${baseName}.ts`);
  const zodOutput = path.join(webGeneratedDir, `${baseName}.schema.ts`);

  const relativeSchemaPath = path.relative(webDir, schemaPath);
  const relativeTsOutput = path.relative(webDir, tsOutput);
  const relativeZodOutput = path.relative(webDir, zodOutput);

  run(
    [
      "npx json2ts",
      `-i ${shellQuote(relativeSchemaPath)}`,
      `-o ${shellQuote(relativeTsOutput)}`,
      `--bannerComment=${shellQuote(TS_BANNER)}`,
    ].join(" "),
    webDir,
  );
  run(
    `npx json-schema-to-zod -i "${relativeSchemaPath}" -n ${zodExportName} -o "${relativeZodOutput}"`,
    webDir,
  );

  const pythonModuleName = toPythonModuleName(baseName);
  const pythonClassName = toPythonClassName(baseName);
  const pythonOutput = path.join(apiGeneratedDir, `${pythonModuleName}.py`);
  const relativeSchemaFromApi = path.relative(apiDir, schemaPath);
  const relativePythonOutput = path.relative(apiDir, pythonOutput);

  run(
    [
      "uv run datamodel-codegen",
      `--input "${relativeSchemaFromApi}"`,
      "--input-file-type jsonschema",
      "--output-model-type pydantic_v2.BaseModel",
      `--output "${relativePythonOutput}"`,
      `--class-name ${pythonClassName}`,
      "--snake-case-field",
      "--allow-population-by-field-name",
      "--disable-timestamp",
      "--formatters builtin",
    ].join(" "),
    apiDir,
  );
}

run(`npx prettier --write "${path.relative(webDir, webGeneratedDir)}"`, webDir);
// Match lint-staged: isort blank lines come from `ruff check --fix`, not `ruff format`.
run("uv run ruff check --fix schemas/generated", apiDir);
run("uv run ruff format schemas/generated", apiDir);
